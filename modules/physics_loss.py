# Smoke test
# ---------------------------------------------------------------------------

if __name__ == "__main__":
    torch.manual_seed(0)
    B = 32

    pred   = torch.randn(B, 100)
    target = torch.randn(B, 102)

    # --- PhysicsLoss forward ---
    physics = PhysicsLoss(lambda_lift=1.0, lambda_smooth=0.01,
                          lambda_drag=0.1, lambda_cdp=0.1)
    losses = physics(pred, target)
    print("PhysicsLoss forward pass:")
    for k, v in losses.items():
        print(f"  {k:10s}: {v.item():.6f}")

    print()

    # --- verify_constraints ---
    verify_constraints(pred, target, n_cp=98, verbose=True)

    print()

    # --- Sanity: perfect prediction should give lift_cp_correlation = 1.0 ---
    # Build a fake pred where Cl IS exactly the ΔCp integral (z-normed)
    cp_ss = torch.zeros(B, 50)
    cp_ps_inner = torch.ones(B, 48)                   # ΔCp = 1 everywhere
    # Build a 98-pt Cp: suction=0, pressure side reversed back
    cp_fake = torch.cat([cp_ss, torch.flip(cp_ps_inner, dims=[1])], dim=1)
    integral = _delta_cp_integral(cp_fake)             # should be ~1.0 per sample
    pred_perfect = torch.cat([
        integral.unsqueeze(1),                         # Cl = integral
        torch.zeros(B, 1),                             # Cd
        cp_fake,                                       # Cp
    ], dim=1)
    results_perfect = verify_constraints(pred_perfect, target, verbose=False)
    print(f"Sanity check — perfect lift correlation: "
          f"{results_perfect['lift_cp_correlation']:.4f}  (expected ~1.0)")
